// Подготовка текста ответа Claude к озвучке (Шаг TTS-B). Чистая функция.
// Ответы часто содержат markdown — для TTS убираем разметку и невокализуемое
// (блоки кода), оставляя читаемый текст.

/** Чистит markdown/служебное для естественной озвучки. */
export function prepareTtsText(input: string): string {
  let t = input

  // Блоки кода не читаем — заменяем на короткую фразу (обычно чанк уже приходит
  // как готовая фраза-заглушка из splitSpeakable; это страховка на сырой текст).
  t = t.replace(/```[\s\S]*?```/g, ' Далее пример кода. ')
  t = t.replace(/~~~[\s\S]*?~~~/g, ' Далее пример кода. ')

  // Инлайн-код: снимаем бэктики, оставляем текст.
  t = t.replace(/`([^`]+)`/g, '$1')

  // Ссылки [текст](url) → текст; картинки ![alt](url) → alt.
  t = t.replace(/!?\[([^\]]*)\]\([^)]*\)/g, '$1')

  // Заголовки, цитаты, маркеры списков в начале строк.
  t = t.replace(/^\s{0,3}#{1,6}\s+/gm, '')
  t = t.replace(/^\s{0,3}>\s?/gm, '')
  t = t.replace(/^\s{0,3}[-*+]\s+/gm, '')
  t = t.replace(/^\s{0,3}\d+\.\s+/gm, '')

  // Эмфаза: **жирный**, *курсив*, __ __, _ _, ~~зачёркнутый~~.
  t = t.replace(/(\*\*|__)(.*?)\1/g, '$2')
  t = t.replace(/(\*|_)(.*?)\1/g, '$2')
  t = t.replace(/~~(.*?)~~/g, '$1')

  // Схлопываем пробелы/переводы строк.
  t = t.replace(/[ \t]+/g, ' ')
  t = t.replace(/\s*\n\s*/g, '\n')
  t = t.replace(/\n{2,}/g, '\n')

  return t.trim()
}
